    @TableId(value = "id", type = IdType.AUTO)
    private Integer id;

    private String name;

    @TableField("collegeId")
    private Integer collegeId;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
